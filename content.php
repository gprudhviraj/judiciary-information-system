  <h2>Indian Courts</h2>

	<dl>
	  <dd>The ‘Indiancourts’ is a bouquet of Web Sites of the Supreme Court and all 21 High Courts and their Benches in India. It provides a single point access to information related to the Supreme Court and any High Court in India. The Web Sites of the Supreme court and High Courts provide Litigant centric dynamic information like Judgments, Causelists, Case-status, etc. as well as static Information such as History, Jurisdiction, Rules, Past and present judges, etc.
	</dd>
	  <dt>The Judgment Information system (JUDIS)</dt>
	  <dd>consists of the Judgments of the Supreme court of India and several High Courts. In the case of the Supreme Court all reported Judgments which are published in SCR Journal, since its inception i.e. 1950 till date are available. The Judgments reported in SCR till 1993 also have head-notes. The judgments reported in SCR in 1994 and later have only text of judgments with out head-notes.
	</dd>
	<dt>Causelists</dt>
	  <dd>are scheduling of cases to be heard by the courts on the following day. The Causelists of Supreme Court and almost all High Courts are available on this site. As the Supreme Court of India and all the 21 High Courts and their 10 Benches are fully computerised, all these courts generate Daily and Weekly Causelists from the computer servers. As soon as they generate the Causelists immediately the are made available on this website.
	</dd>
	<dt>Daily Orders</dt>
	  <dd>of the Supreme Court of India and many High Courts can be accessed through this site. These orders are available as soon as the orders are signed by the court.
	</dd>
	<dt>Case Status</dt>
	  <dd>site provides the latest status of a case either pending or disposed by the Supreme Court or any other High Court in the country. The required information is derived from the databases of the concerned courts.
	</dd>
	<dt>IndianCode</dt>
	  <dd>the India Code Information System contains all Central Acts of Parliament right from 1834 onwards. Each Act includes: Short Title, Enactment Date, Sections, Schedule and also Footnotes. Besides this, Statement of Objects and Reasons (SOR),Table of contents and Status of an Act is also available in IndiacCode.</dd>
	</dl>
